package io.lettuce.core.api.coroutines

import io.lettuce.core.ExperimentalLettuceCoroutinesApi
import io.lettuce.core.Pair
import io.lettuce.core.probabilistic.TopKInfoValue
import io.lettuce.core.probabilistic.TopKListValue
import io.lettuce.core.probabilistic.arguments.TopKReserveArgs

/**
 * Coroutine executed commands for Top-K.
 *
 * @author Yordan Tsintsov
 * @param <K> Key type.
 * @param <V> Value type.
 * @see <a href="https://redis.io/docs/latest/develop/data-types/probabilistic/top-k/">Redis Top-K</a>
 * @since 7.7
 * @generated by io.lettuce.apigenerator.CreateKotlinCoroutinesApi
 */
@ExperimentalLettuceCoroutinesApi
interface RedisTopKCoroutinesCommands<K : Any, V : Any> {

    /**
     * Adds a `value` to a Top-k sketch. If a `value` enters the Top-K sketch, the value that is expelled (if any)
     * is returned. This allows dynamic heavy-hitter detection of values being entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param value the value.
     * @return List<String> where each element is the value expelled from the Top-K sketch when the corresponding value
     *         entered it, or `null` if no value was expelled.
     */
    suspend fun topKAdd(key: K, value: V): List<String?>

    /**
     * Adds a `value` to a Top-k sketch. Multiple values can be added at the same time. If a `value` enters the
     * Top-K sketch, the value that is expelled (if any) is returned. This allows dynamic heavy-hitter detection of values being
     * entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param values the values.
     * @return List<String> where each element is the value expelled from the Top-K sketch when the corresponding value
     *         entered it, or `null` if no value was expelled.
     */
    suspend fun topKAdd(key: K, vararg values: V): List<String?>

    /**
     * Adds a `value` to a Top-k sketch. If the `value` enters the Top-K sketch, the value that is expelled (if any)
     * is returned. This allows dynamic heavy-hitter detection of values being entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param pair the value and count pair.
     * @return List<String> where each element is the value expelled from the Top-K sketch when the corresponding value
     *         entered it, or `null` if no value was expelled.
     */
    suspend fun topKIncrBy(key: K, pair: Pair<V, Long>): List<String?>

    /**
     * Adds a `value` to a Top-k sketch. Multiple values can be added at the same time. If a `value` enters the
     * Top-K sketch, the value that is expelled (if any) is returned. This allows dynamic heavy-hitter detection of values being
     * entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param pairs the value and count pairs.
     * @return List<String> where each element is the value expelled from the Top-K sketch when the corresponding value
     *         entered it, or `null` if no value was expelled.
     */
    suspend fun topKIncrBy(key: K, vararg pairs: Pair<V, Long>): List<String?>

    /**
     * Returns information about the Top-K sketch.
     *
     * @param key the key.
     * @return @link TopKInfoValue} the information about the sketch.
     */
    suspend fun topKInfo(key: K): TopKInfoValue?

    /**
     * Returns the full list of values in Top-K sketch.
     *
     * @param key the key.
     * @return List<String> containing the current top-k values in the sketch.
     */
    suspend fun topKList(key: K): List<String>

    /**
     * Returns the full list of values in Top-K sketch paired with their count if `withCount` is true.
     *
     * @param key the key.
     * @param withCount whether to return the count of each value.
     * @return List<TopKListValue> containing the current top-k values in the sketch paired with their count.
     */
    suspend fun topKList(key: K, withCount: Boolean): List<TopKListValue>

    /**
     * Checks whether `value` is one of the top-k elements of the sketch.
     *
     * @param key the key.
     * @param value the value.
     * @return List<Boolean> where `true` means the corresponding value is one of the top-k elements of the sketch,
     *         `false` otherwise.
     */
    suspend fun topKQuery(key: K, value: V): List<Boolean>

    /**
     * Checks whether `values` are in the Top-K sketch.
     *
     * @param key the key.
     * @param values the values.
     * @return List<Boolean> where `true` means the corresponding value is one of the top-k elements of the sketch,
     *         `false` otherwise.
     */
    suspend fun topKQuery(key: K, vararg values: V): List<Boolean>

    /**
     * Initializes a Top-K sketch with specified parameters.
     *
     * @param key the key.
     * @param k the maximum number of values to keep.
     * @return String simple-string-reply `OK` if the sketch was created.
     */
    suspend fun topKReserve(key: K, k: Long): String?

    /**
     * Initializes a Top-K sketch with specified parameters.
     *
     * @param key the key.
     * @param k the maximum number of values to keep.
     * @param args the reserve arguments.
     * @return String simple-string-reply `OK` if the sketch was created.
     */
    suspend fun topKReserve(key: K, k: Long, args: TopKReserveArgs): String?

}

