package io.lettuce.core.api.coroutines

import io.lettuce.core.ExperimentalLettuceCoroutinesApi
import kotlinx.coroutines.flow.Flow
import io.lettuce.core.probabilistic.BfInfoValue
import io.lettuce.core.probabilistic.arguments.BfInsertArgs
import io.lettuce.core.probabilistic.arguments.BfReserveArgs
import io.lettuce.core.probabilistic.ScanDumpValue

/**
 * Coroutine executed commands for Bloom Filter.
 *
 * @author Yordan Tsintsov
 * @param <K> Key type.
 * @param <V> Value type.
 * @see <a href="https://redis.io/docs/latest/develop/data-types/probabilistic/bloom-filter/">Redis Bloom Filter</a>
 * @since 7.7
 * @generated by io.lettuce.apigenerator.CreateKotlinCoroutinesApi
 */
@ExperimentalLettuceCoroutinesApi
interface RedisBloomFilterCoroutinesCommands<K : Any, V : Any> {

    /**
     * Add an item to the Bloom Filter.
     *
     * @param key the key.
     * @param value the value.
     * @return Boolean integer-reply `true` if the item was added, `false` if it was already in the filter.
     */
    suspend fun bfAdd(key: K, value: V): Boolean?

    /**
     * Get the number of items added and detected as unique in the Bloom Filter.
     *
     * @param key the key.
     * @return Long integer-reply the number of items in the filter.
     */
    suspend fun bfCard(key: K): Long?

    /**
     * Check if an item exists in the Bloom Filter.
     *
     * @param key the key.
     * @param value the value.
     * @return Boolean integer-reply `true` if the item may exist in the filter, `false` if it definitely does not.
     */
    suspend fun bfExists(key: K, value: V): Boolean?

    /**
     * Get information about the Bloom Filter.
     *
     * @param key the key.
     * @return BfInfoValue the information about the filter.
     */
    suspend fun bfInfo(key: K): BfInfoValue?

    /**
     * Add an item to the Bloom Filter.
     *
     * @param key the key.
     * @param value the value.
     * @return List<Boolean> where `true` means that the item was added; `false` means that there's a
     *         probability that the item was already added to the filter or the filter is full; `null` when filter
     *         is full and nonscaling.
     */
    suspend fun bfInsert(key: K, value: V): List<Boolean?>

    /**
     * Add an item to the Bloom Filter.
     *
     * @param key the key.
     * @param insertArgs the insert arguments.
     * @param value the value.
     * @return List<Boolean> where `true` means that the item was added; `false` means that there's a
     *         probability that the item was already added to the filter or the filter is full; `null` when filter
     *         is full and nonscaling.
     */
    suspend fun bfInsert(key: K, insertArgs: BfInsertArgs, value: V): List<Boolean?>

    /**
     * Add one or more items to the Bloom Filter.
     *
     * @param key the key.
     * @param values the values.
     * @return List<Boolean> where `true` means that the item was added; `false` means that there's a
     *         probability that the item was already added to the filter or the filter is full; `null` when filter
     *         is full and nonscaling.
     */
    suspend fun bfInsert(key: K, vararg values: V): List<Boolean?>

    /**
     * Add one or more items to the Bloom Filter.
     *
     * @param key the key.
     * @param insertArgs the insert arguments.
     * @param values the values.
     * @return List<Boolean> where `true` means that the item was added; `false` means that there's a
     *         probability that the item was already added to the filter or the filter is full; `null` when filter
     *         is full and nonscaling.
     */
    suspend fun bfInsert(key: K, insertArgs: BfInsertArgs, vararg values: V): List<Boolean?>

    /**
     * Restores a Bloom filter previously saved using BF.SCANDUMP.
     *
     * @param key the key.
     * @param iterator the iterator.
     * @param data the data.
     * @return String simple-string-reply `OK` if `BF.LOADCHUNK` was executed correctly.
     */
    suspend fun bfLoadChunk(key: K, iterator: Long, data: ByteArray): String?

    /**
     * Add one or more items to the Bloom Filter.
     *
     * @param key the key.
     * @param values the values.
     * @return List<Boolean> of `true` or `false` where true means that the item was newly added and false
     *         means that there's a probability that the item was already added to the filter; `null` when filter
     *         is full and nonscaling.
     */
    suspend fun bfMAdd(key: K, vararg values: V): List<Boolean?>

    /**
     * Check if one or more items exist in the filter.
     *
     * @param key the key.
     * @param values the values.
     * @return List<Boolean> of `true` or `false` where true means that, with high probability, the item was
     *         already added to the filter, and false means that key does not exist or that item was definitely not added to the
     *         filter.
     */
    suspend fun bfMExists(key: K, vararg values: V): List<Boolean>

    /**
     * Creates an empty Bloom filter with a single sub-filter for the initial specified capacity and with an upper bound error
     * rate.
     *
     * @param key the key.
     * @param errorRate the error rate.
     * @param capacity the capacity.
     * @return String simple-string-reply `OK` if `BF.RESERVE` was executed correctly.
     */
    suspend fun bfReserve(key: K, errorRate: Double, capacity: Long): String?

    /**
     * Creates an empty Bloom filter with a single sub-filter for the initial specified capacity and with an upper bound error
     * rate.
     *
     * @param key the key.
     * @param errorRate the error rate.
     * @param capacity the capacity.
     * @param reserveArgs the reserve arguments.
     * @return String simple-string-reply `OK` if `BF.RESERVE` was executed correctly.
     */
    suspend fun bfReserve(key: K, errorRate: Double, capacity: Long, reserveArgs: BfReserveArgs): String?

    /**
     * Begins an incremental save of the Bloom filter.
     *
     * @param key the key.
     * @param iterator the iterator.
     * @return ScanDumpValue the scan dump value.
     */
    suspend fun bfScanDump(key: K, iterator: Long): ScanDumpValue?

}

