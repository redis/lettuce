package io.lettuce.core.api.reactive;

import java.util.List;
import java.util.Map;

import io.lettuce.core.timeseries.TsAggregationType;
import io.lettuce.core.timeseries.TsInfoValue;
import io.lettuce.core.timeseries.TsMGetValue;
import io.lettuce.core.timeseries.TsSample;
import io.lettuce.core.timeseries.arguments.TsAddArgs;
import io.lettuce.core.timeseries.arguments.TsAlterArgs;
import io.lettuce.core.timeseries.arguments.TsCreateArgs;
import io.lettuce.core.timeseries.arguments.TsGetArgs;
import io.lettuce.core.timeseries.arguments.TsIncrByArgs;
import io.lettuce.core.timeseries.arguments.TsMGetArgs;
import reactor.core.publisher.Flux;
import reactor.core.publisher.Mono;

/**
 * Reactive executed commands for RedisTimeSeries.
 *
 * @author Gyumin Hwang
 * @param <K> Key type.
 * @param <V> Value type.
 * @see <a href="https://redis.io/docs/latest/develop/data-types/timeseries/">Redis Time Series</a>
 * @since 7.7
 * @generated by io.lettuce.apigenerator.CreateReactiveApi
 */
public interface RedisTimeSeriesReactiveCommands<K, V> {

    /**
     * Create a new time series.
     *
     * @param key the key.
     * @return String simple-string-reply {@code OK} if {@code TS.CREATE} was executed correctly.
     */
    Mono<String> tsCreate(K key);

    /**
     * Create a new time series.
     *
     * @param key the key.
     * @param createArgs the create arguments.
     * @return String simple-string-reply {@code OK} if {@code TS.CREATE} was executed correctly.
     */
    Mono<String> tsCreate(K key, TsCreateArgs createArgs);

    /**
     * Update the retention, chunk size, duplicate policy, and/or labels of an existing time series.
     *
     * @param key the key.
     * @param alterArgs the alter arguments.
     * @return String simple-string-reply {@code OK} if {@code TS.ALTER} was executed correctly.
     */
    Mono<String> tsAlter(K key, TsAlterArgs alterArgs);

    /**
     * Create a compaction rule.
     *
     * @param sourceKey the source key.
     * @param destKey the destination key, that should be already created.
     * @param aggregationType the aggregation type.
     * @param bucketDuration the bucket duration, in milliseconds.
     * @return String simple-string-reply {@code OK} if {@code TS.CREATERULE} was executed correctly.
     */
    Mono<String> tsCreateRule(K sourceKey, K destKey, TsAggregationType aggregationType, long bucketDuration);

    /**
     * Create a compaction rule.
     *
     * @param sourceKey the source key.
     * @param destKey the destination key, that should be already created.
     * @param aggregationType the aggregation type.
     * @param bucketDuration the bucket duration, in milliseconds.
     * @param alignTimestamp ensures that there is a bucket that starts exactly at this timestamp, in milliseconds.
     * @return String simple-string-reply {@code OK} if {@code TS.CREATERULE} was executed correctly.
     */
    Mono<String> tsCreateRule(K sourceKey, K destKey, TsAggregationType aggregationType, long bucketDuration,
            long alignTimestamp);

    /**
     * Delete a compaction rule.
     *
     * @param sourceKey the source key.
     * @param destKey the destination key.
     * @return String simple-string-reply {@code OK} if {@code TS.DELETERULE} was executed correctly.
     */
    Mono<String> tsDeleteRule(K sourceKey, K destKey);

    /**
     * Delete all samples between two timestamps for a given time series.
     *
     * @param key the key.
     * @param fromTimestamp start timestamp, in milliseconds.
     * @param toTimestamp end timestamp, in milliseconds.
     * @return Long integer-reply the number of samples that were removed.
     */
    Mono<Long> tsDel(K key, long fromTimestamp, long toTimestamp);

    /**
     * Append a sample to a time series, creating the series automatically if it does not yet exist.
     *
     * @param key the key.
     * @param timestamp the sample timestamp, in milliseconds.
     * @param value the sample value.
     * @return Long integer-reply the timestamp that was ultimately used for the sample.
     */
    Mono<Long> tsAdd(K key, long timestamp, double value);

    /**
     * Append a sample to a time series, creating the series automatically if it does not yet exist.
     *
     * @param key the key.
     * @param timestamp the sample timestamp, in milliseconds.
     * @param value the sample value.
     * @param addArgs the add arguments.
     * @return Long integer-reply the timestamp that was ultimately used for the sample.
     */
    Mono<Long> tsAdd(K key, long timestamp, double value, TsAddArgs addArgs);

    /**
     * Append a sample to a time series, letting the server assign the current time as the sample timestamp, creating the series
     * automatically if it does not yet exist.
     *
     * @param key the key.
     * @param value the sample value.
     * @return Long integer-reply the timestamp that was ultimately used for the sample.
     */
    Mono<Long> tsAdd(K key, double value);

    /**
     * Append samples to multiple time series at once, creating any of the series automatically if it does not yet exist.
     *
     * @param entries the (key, sample) entries to append; each {@link TsSample} must carry a single value.
     * @return List&lt;Long&gt; the timestamps that were ultimately used, in the same order as {@code entries}.
     * @throws IllegalArgumentException if any {@link TsSample} carries more than one value.
     */
    Flux<Long> tsMAdd(Map.Entry<K, TsSample>... entries);

    /**
     * Append samples to multiple time series at once, creating any of the series automatically if it does not yet exist.
     *
     * @param entry the (key, sample) entry to append; the {@link TsSample} must carry a single value.
     * @return List&lt;Long&gt; the timestamps that were ultimately used, in the same order as {@code entries}.
     * @throws IllegalArgumentException if the {@link TsSample} carries more than one value.
     */
    Flux<Long> tsMAdd(Map.Entry<K, TsSample> entry);

    /**
     * Increment the value of the last sample of a time series, creating the series automatically if it does not yet exist.
     *
     * @param key the key.
     * @param value the value to add to the last sample.
     * @return Long integer-reply the timestamp of the updated sample.
     */
    Mono<Long> tsIncrBy(K key, double value);

    /**
     * Increment the value of the last sample of a time series, creating the series automatically if it does not yet exist.
     *
     * @param key the key.
     * @param value the value to add to the last sample.
     * @param incrByArgs the increment-by arguments.
     * @return Long integer-reply the timestamp of the updated sample.
     */
    Mono<Long> tsIncrBy(K key, double value, TsIncrByArgs incrByArgs);

    /**
     * Decrement the value of the last sample of a time series, creating the series automatically if it does not yet exist.
     *
     * @param key the key.
     * @param value the value to subtract from the last sample.
     * @return Long integer-reply the timestamp of the updated sample.
     */
    Mono<Long> tsDecrBy(K key, double value);

    /**
     * Decrement the value of the last sample of a time series, creating the series automatically if it does not yet exist.
     *
     * @param key the key.
     * @param value the value to subtract from the last sample.
     * @param decrByArgs the decrement-by arguments.
     * @return Long integer-reply the timestamp of the updated sample.
     */
    Mono<Long> tsDecrBy(K key, double value, TsIncrByArgs decrByArgs);

    /**
     * Get the last sample of a time series.
     *
     * @param key the key.
     * @return TsSample the last sample of the time series, or {@code null} if the series has no samples.
     */
    Mono<TsSample> tsGet(K key);

    /**
     * Get the last sample of a time series.
     *
     * @param key the key.
     * @param getArgs the get arguments.
     * @return TsSample the last sample of the time series, or {@code null} if the series has no samples.
     */
    Mono<TsSample> tsGet(K key, TsGetArgs getArgs);

    /**
     * Get the last samples of multiple time series matching one or more label filters.
     *
     * @param filters one or more label filters, at least one of which must be an equality filter.
     * @return List&lt;TsMGetValue&lt;K&gt;&gt; the last sample and, depending on {@link TsMGetArgs}, the labels of each
     *         matching time series.
     * @throws io.lettuce.core.RedisCommandExecutionException if no filter is an equality filter.
     */
    Flux<TsMGetValue<K>> tsMGet(V... filters);

    /**
     * Get the last samples of multiple time series matching one or more label filters.
     *
     * @param filter one or more label filters, at least one of which must be an equality filter.
     * @return List&lt;TsMGetValue&lt;K&gt;&gt; the last sample and, depending on {@link TsMGetArgs}, the labels of each
     *         matching time series.
     * @throws io.lettuce.core.RedisCommandExecutionException if no filter is an equality filter.
     */
    Flux<TsMGetValue<K>> tsMGet(V filter);

    /**
     * Get the last samples of multiple time series matching one or more label filters.
     *
     * @param mGetArgs the get arguments.
     * @param filters one or more label filters, at least one of which must be an equality filter.
     * @return List&lt;TsMGetValue&lt;K&gt;&gt; the last sample and, depending on {@link TsMGetArgs}, the labels of each
     *         matching time series.
     * @throws IllegalArgumentException if {@code mGetArgs} combines {@code WITHLABELS} and {@code SELECTED_LABELS}.
     * @throws io.lettuce.core.RedisCommandExecutionException if no filter is an equality filter.
     */
    Flux<TsMGetValue<K>> tsMGet(TsMGetArgs mGetArgs, V... filters);

    /**
     * Get the last samples of multiple time series matching one or more label filters.
     *
     * @param mGetArgs the get arguments.
     * @param filter one or more label filters, at least one of which must be an equality filter.
     * @return List&lt;TsMGetValue&lt;K&gt;&gt; the last sample and, depending on {@link TsMGetArgs}, the labels of each
     *         matching time series.
     * @throws IllegalArgumentException if {@code mGetArgs} combines {@code WITHLABELS} and {@code SELECTED_LABELS}.
     * @throws io.lettuce.core.RedisCommandExecutionException if no filter is an equality filter.
     */
    Flux<TsMGetValue<K>> tsMGet(TsMGetArgs mGetArgs, V filter);

    /**
     * Get metadata about a time series.
     *
     * @param key the key.
     * @return TsInfoValue metadata about the time series.
     */
    Mono<TsInfoValue<K>> tsInfo(K key);

    /**
     * Get metadata about a time series, including chunk-level debug information.
     *
     * @param key the key.
     * @return TsInfoValue metadata about the time series, including chunk-level debug information.
     */
    Mono<TsInfoValue<K>> tsInfoDebug(K key);

    /**
     * Get all time series keys matching one or more label filters.
     *
     * @param filters one or more label filters, at least one of which must be an equality filter.
     * @return List&lt;K&gt; the keys of the matching time series; order is not guaranteed.
     * @throws io.lettuce.core.RedisCommandExecutionException if no filter is an equality filter.
     */
    Flux<K> tsQueryIndex(V... filters);

    /**
     * Get all time series keys matching one or more label filters.
     *
     * @param filter one or more label filters, at least one of which must be an equality filter.
     * @return List&lt;K&gt; the keys of the matching time series; order is not guaranteed.
     * @throws io.lettuce.core.RedisCommandExecutionException if no filter is an equality filter.
     */
    Flux<K> tsQueryIndex(V filter);

}
