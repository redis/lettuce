package io.lettuce.core.api.reactive;

import io.lettuce.core.Pair;
import io.lettuce.core.Value;
import io.lettuce.core.probabilistic.TopKInfoValue;
import io.lettuce.core.probabilistic.TopKListValue;
import io.lettuce.core.probabilistic.arguments.TopKReserveArgs;
import reactor.core.publisher.Flux;
import reactor.core.publisher.Mono;

/**
 * Reactive executed commands for Top-K.
 *
 * @author Yordan Tsintsov
 * @param <K> Key type.
 * @param <V> Value type.
 * @see <a href="https://redis.io/docs/latest/develop/data-types/probabilistic/top-k/">Redis Top-K</a>
 * @since 7.7
 * @generated by io.lettuce.apigenerator.CreateReactiveApi
 */
public interface RedisTopKReactiveCommands<K, V> {

    /**
     * Adds a {@code value} to a Top-k sketch. If a {@code value} enters the Top-K sketch, the value that is expelled (if any)
     * is returned. This allows dynamic heavy-hitter detection of values being entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param value the value.
     * @return String where each element is the value expelled from the Top-K sketch when the corresponding value entered it, or
     *         {@code null} if no value was expelled.
     */
    Flux<Value<String>> topKAdd(K key, V value);

    /**
     * Adds a {@code value} to a Top-k sketch. Multiple values can be added at the same time. If a {@code value} enters the
     * Top-K sketch, the value that is expelled (if any) is returned. This allows dynamic heavy-hitter detection of values being
     * entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param values the values.
     * @return String where each element is the value expelled from the Top-K sketch when the corresponding value entered it, or
     *         {@code null} if no value was expelled.
     */
    Flux<Value<String>> topKAdd(K key, V... values);

    /**
     * Adds a {@code value} to a Top-k sketch. If the {@code value} enters the Top-K sketch, the value that is expelled (if any)
     * is returned. This allows dynamic heavy-hitter detection of values being entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param pair the value and count pair.
     * @return String where each element is the value expelled from the Top-K sketch when the corresponding value entered it, or
     *         {@code null} if no value was expelled.
     */
    Flux<Value<String>> topKIncrBy(K key, Pair<V, Long> pair);

    /**
     * Adds a {@code value} to a Top-k sketch. Multiple values can be added at the same time. If a {@code value} enters the
     * Top-K sketch, the value that is expelled (if any) is returned. This allows dynamic heavy-hitter detection of values being
     * entered or expelled from Top-K sketch.
     *
     * @param key the key.
     * @param pairs the value and count pairs.
     * @return String where each element is the value expelled from the Top-K sketch when the corresponding value entered it, or
     *         {@code null} if no value was expelled.
     */
    Flux<Value<String>> topKIncrBy(K key, Pair<V, Long>... pairs);

    /**
     * Returns information about the Top-K sketch.
     *
     * @param key the key.
     * @return {@link TopKInfoValue} the information about the sketch.
     */
    Mono<TopKInfoValue> topKInfo(K key);

    /**
     * Returns the full list of values in Top-K sketch.
     *
     * @param key the key.
     * @return String containing the current top-k values in the sketch.
     */
    Flux<String> topKList(K key);

    /**
     * Returns the full list of values in Top-K sketch paired with their count if {@code withCount} is true.
     *
     * @param key the key.
     * @param withCount whether to return the count of each value.
     * @return TopKListValue containing the current top-k values in the sketch paired with their count.
     */
    Flux<TopKListValue> topKList(K key, boolean withCount);

    /**
     * Checks whether {@code value} is one of the top-k elements of the sketch.
     *
     * @param key the key.
     * @param value the value.
     * @return Boolean where {@code true} means the corresponding value is one of the top-k elements of the sketch,
     *         {@code false} otherwise.
     */
    Flux<Boolean> topKQuery(K key, V value);

    /**
     * Checks whether {@code values} are in the Top-K sketch.
     *
     * @param key the key.
     * @param values the values.
     * @return Boolean where {@code true} means the corresponding value is one of the top-k elements of the sketch,
     *         {@code false} otherwise.
     */
    Flux<Boolean> topKQuery(K key, V... values);

    /**
     * Initializes a Top-K sketch with specified parameters.
     *
     * @param key the key.
     * @param k the maximum number of values to keep.
     * @return String simple-string-reply {@code OK} if the sketch was created.
     */
    Mono<String> topKReserve(K key, long k);

    /**
     * Initializes a Top-K sketch with specified parameters.
     *
     * @param key the key.
     * @param k the maximum number of values to keep.
     * @param args the reserve arguments.
     * @return String simple-string-reply {@code OK} if the sketch was created.
     */
    Mono<String> topKReserve(K key, long k, TopKReserveArgs args);

}
